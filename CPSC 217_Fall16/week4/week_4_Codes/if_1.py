
num = float(input("Enter any real number : "))

if (num < 0) :
	print('The given number is negative...')

if (num > 0) :
	print('The given number is positive...')

if (num == 0) :
	print('The given number is neutral...')

if (num != 0) :
	print('The given number is nonzero...')

if (num >= 0 and num <= 100) :
	print('The given number is within 0 to 100...')

if (num < 0 or num > 100) :
	print('The given number is not within 0 to 100...')